def decode_str(s) -> str:
    if not s:
        return ""
    parts = decode_header(s)
    result = []
    for part, enc in parts:
        if isinstance(part, bytes):
            result.append(part.decode(enc or "utf-8", errors="replace"))
        else:
            result.append(str(part))
    return "".join(result).strip()


def _clean_text(text: str) -> str:
    """Strip tracking URLs and excessive whitespace."""
    text = re.sub(r'<https?://\S+>', '', text)
    text = re.sub(r'https?://\S+', '', text)
    text = re.sub(r'\n{3,}', '\n\n', text)
    return text.strip()


def _extract_pdf_text(data: bytes) -> str:
    """Extract text from a PDF attachment. Returns '' on failure."""
    import warnings, logging
    logging.getLogger("pdfminer").setLevel(logging.ERROR)
    try:
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            with pdfplumber.open(io.BytesIO(data)) as pdf:
                pages = [p.extract_text() or "" for p in pdf.pages[:6]]  # cap at 6 pages
                return _clean_text("\n\n".join(pages))[:3000]
    except Exception:
        return ""


def get_body_and_attachments(msg) -> tuple:
    """
    Returns (body: str, attachments: list[dict])
    Each attachment dict: { name, type, text }
      - text is populated for PDFs, empty for other types
    """
    body = ""
    attachments = []

    if msg.is_multipart():
        for part in msg.walk():
            disposition = str(part.get("Content-Disposition", ""))
            content_type = part.get_content_type()
            filename = part.get_filename()

            if filename:
                # It's an attachment
                raw_data = part.get_payload(decode=True) or b""
                att = {
                    "name": decode_str(filename),
                    "type": "pdf" if filename.lower().endswith(".pdf") else
                            "image" if content_type.startswith("image/") else "other",
                    "text": ""
                }
                if att["type"] == "pdf":
                    att["text"] = _extract_pdf_text(raw_data)
                attachments.append(att)

            elif content_type == "text/plain" and "attachment" not in disposition and not body:
                charset = part.get_content_charset() or "utf-8"
                body = part.get_payload(decode=True).decode(charset, errors="replace")
    else:
        charset = msg.get_content_charset() or "utf-8"
        payload = msg.get_payload(decode=True)
        if payload:
            body = payload.decode(charset, errors="replace")

    body = _clean_text(body)[:4000]
    return body, attachments


def fetch_all_emails_for_date(mail: imaplib.IMAP4_SSL, target_date: date) -> list[dict]:
    """Fetch every email in the inbox received on target_date."""
    mail.select("INBOX")
    imap_since  = target_date.strftime("%d-%b-%Y")
    imap_before = (target_date + timedelta(days=1)).strftime("%d-%b-%Y")

    _, msg_ids = mail.search(None, f'(SINCE {imap_since} BEFORE {imap_before})')
    ids = msg_ids[0].split()
    print(f"   {len(ids)} email(s) found on {target_date}")

    emails = []
    for uid in ids:
        _, msg_data = mail.fetch(uid, "(RFC822)")
        raw = msg_data[0][1]
        msg = email.message_from_bytes(raw)
        body, attachments = get_body_and_attachments(msg)
        emails.append({
            "subject":     decode_str(msg.get("Subject", "")),
            "from":        decode_str(msg.get("From", "")),
            "body":        body,
            "attachments": attachments,
        })
    return emails


