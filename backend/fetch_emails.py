def _stamp_todo_ids(todos: list, date_str: str, kid: str) -> list:
    """Assign stable IDs to todo items based on content hash."""
    import hashlib
    result = []
    for t in todos:
        text = t.get("text", "")
        raw_id = f"{kid}-{date_str}-{text}"
        stable_id = kid[0] + "-" + hashlib.md5(raw_id.encode()).hexdigest()[:10]
        result.append({**t, "id": stable_id, "done": False})
    return result


def build_arjun_day_summary(processed: list[dict], subjects: list[str], target_date: date) -> str:
    day_name = target_date.strftime("%A")
    date_str = target_date.isoformat()
    if not processed:
        return (
            f"No school emails received today ({day_name}, {date_str}).\n\n"
            f"Today's subjects: {', '.join(subjects)}."
        )
    bullets = "\n".join(f"- {e['subject']}: {e['summary']}" for e in processed)
    resp = _get_client().models.generate_content(
        model="gemini-2.5-flash",
        contents=(
            f"Write a 3-4 sentence daily briefing for parents about their Grade 7 child's school day.\n"
            f"Today is {day_name}, {date_str}. Subjects: {', '.join(subjects)}.\n\n"
            f"Email summaries:\n{bullets}\n\n"
            "Plain, warm, flowing sentences. No bullet points."
        ),
    )
    return resp.text.strip()


def build_myra_day_summary(processed: list[dict], target_date: date) -> str:
    if not processed:
        return f"No school emails received ({target_date.strftime('%A')}, {target_date.isoformat()})."
    return "\n".join(f"• {e['subject']}: {e['summary']}" for e in processed)


# ── Process one day ─────────────────────────────────────────

def process_day(target_date: date, data: dict, timetable: dict, mail: imaplib.IMAP4_SSL):
    day_name = target_date.strftime("%A")
    date_str = target_date.isoformat()
    today_subjects = timetable.get("timetable", {}).get(day_name, [])
    core_today = [s for s in today_subjects if s in CORE_SUBJECTS]

    print(f"\n📅  {date_str} ({day_name})")
    raw_emails = fetch_all_emails_for_date(mail, target_date)

    arjun_processed, myra_processed = [], []
    all_hw, all_vc, all_topics, all_exams, all_holidays = [], [], [], [], []

    import time as _time
    for raw in raw_emails:
        print(f"   🔍 {raw['subject'][:70]}")
        result = classify_and_summarise(raw, target_date, core_today)
        _time.sleep(5)  # stay within Gemini free tier: 15 req/min
        if result is None:
            continue

        if result["kid"] == "arjun":
            all_hw.extend(result.pop("_homeworkItems", []))
            all_vc.extend(result.pop("_veracrossItems", []))
            # New: topics, exams, holidays
            for t in result.pop("_topicsCovered", []):
                t["date"]   = date_str
                t["source"] = result.get("subject", "")
                all_topics.append(t)
            for e in result.pop("_examSchedule", []):
                all_exams.append(e)
            for h in result.pop("_holidays", []):
                all_holidays.append(h)
            result.pop("kid")
            arjun_processed.append(result)
            print(f"       → Arjun ({'·'.join(result.get('tags', [])) or 'general'})")

        elif result["kid"] == "myra":
            result.pop("kid")
            myra_processed.append(result)
            print(f"       → Myra ({result.get('category', 'general')})")

    # ── Arjun's day entry ───────────────────────────────────
    arjun_summary = build_arjun_day_summary(arjun_processed, core_today, target_date)
    arjun_day = {
        "date":           date_str,
        "summary":        arjun_summary,
        "emails":         arjun_processed,
        "veracrossItems": all_vc,
        "topicsCovered":  [t["topic"] for t in all_topics],
        "homeworkItems":  all_hw,
    }
    idx = next((i for i, d in enumerate(data["days"]) if d["date"] == date_str), None)
    if idx is not None:
        data["days"][idx] = arjun_day
    else:
        data["days"].insert(0, arjun_day)

    # Veracross log
    if all_vc:
        vc_idx = next((i for i, v in enumerate(data["veracrossLog"]) if v["date"] == date_str), None)
        if vc_idx is not None:
            data["veracrossLog"][vc_idx]["items"] = all_vc
        else:
            data["veracrossLog"].insert(0, {"date": date_str, "items": all_vc})

    # Topic log — cumulative, deduplicate by subject+date+first-50-chars-of-topic
    topic_log = data.setdefault("topicLog", [])
    for t in all_topics:
        topic_prefix = t["topic"][:50].lower().strip()
        existing = next(
            (x for x in topic_log
             if x["subject"] == t["subject"]
             and x["date"] == date_str
             and x["topic"][:50].lower().strip() == topic_prefix),
            None
        )
        if not existing:
            topic_log.insert(0, t)
            print(f"       📚 Topic: [{t['subject']}] {t['topic'][:60]}")

    # Exam schedule — deduplicate by subject+date; skip non-Grade-7 exams
    GRADE7_SKIP_KEYWORDS = ["grade 11", "grade 12", "ibdp", "ib dp", "xi ", "xii ", " x ", " x\n",
                             "cbse board", "class 11", "class 12", "class x ", "class xi", "class xii"]
    exam_sched = data.setdefault("examSchedule", [])
    for e in all_exams:
        subj_lower   = e.get("subject", "").lower()
        topics_lower = e.get("topics", "").lower()
        # Skip if this exam is clearly for senior grades, not Grade 7
        if any(kw in subj_lower or kw in topics_lower for kw in GRADE7_SKIP_KEYWORDS):
            continue
        existing = next(
            (x for x in exam_sched if x["subject"] == e["subject"] and x["examDate"] == e["examDate"]),
            None
        )
        if not existing:
            exam_sched.insert(0, e)
            print(f"       📝 Exam: [{e.get('examType','')}] {e['subject']} on {e['examDate']}")

    # Holidays — deduplicate by date
    holiday_list = data.setdefault("holidays", [])
    for h in all_holidays:
        existing = next((x for x in holiday_list if x["date"] == h["date"]), None)
        if not existing:
            holiday_list.insert(0, h)
            print(f"       🎉 Holiday: {h['name']} on {h['date']}")

    # ── Myra's day entry ────────────────────────────────────
    myra_summary = build_myra_day_summary(myra_processed, target_date)
    myra_day = {
        "date":    date_str,
        "summary": myra_summary,
        "emails":  myra_processed,
    }
    myra_days = data.setdefault("myra", {}).setdefault("days", [])
    m_idx = next((i for i, d in enumerate(myra_days) if d["date"] == date_str), None)
    if m_idx is not None:
        myra_days[m_idx] = myra_day
    else:
        myra_days.insert(0, myra_day)


# ── Entry point ─────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description="Fetch school emails and update dashboard")
    parser.add_argument(
        "--days", type=int, default=1,
        help="How many past days to fetch (default: 1 = today only)"
    )
    args = parser.parse_args()

    print("🏫  School Dashboard Email Fetcher")
    print(f"    Inbox : {GMAIL_USER}")
    print(f"    Days  : {args.days}")

    data      = load_data()
    timetable = json.loads(TIMETABLE_FILE.read_text(encoding="utf-8"))

    print("\n📧  Connecting to Gmail IMAP...")
    mail = connect_gmail()
    print("    Connected ✓")

    for offset in range(args.days):
        target = date.today() - timedelta(days=offset)
        process_day(target, data, timetable, mail)

    mail.logout()

    data["days"].sort(key=lambda d: d["date"], reverse=True)
    data["myra"]["days"].sort(key=lambda d: d["date"], reverse=True)

    save_data(data)
    save_config()
    print("\n✅  Done!")


if __name__ == "__main__":
    main()
